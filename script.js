let selectedQuestions = [];
let userAnswers = [];
let currentQuestionIndex = 0;
let quizTimer = null;

function startQuiz() {
    const subject = document.getElementById('subject').value;
    const numQuestions = document.getElementById('num-questions').value;
    const quizTime = document.getElementById('quiz-time').value;
    
    selectedQuestions = getRandomQuestions(subject, numQuestions);
    displayQuestion();
    displayQuestionNumbers();
    document.getElementById('quiz-settings').classList.add('hidden');
    document.getElementById('quiz-navigation').classList.remove('hidden');
    document.getElementById('quiz-container').classList.remove('hidden');

    if (quizTime > 0) {
        startTimer(quizTime * 60);
        document.getElementById('timer-container').classList.remove('hidden');
    }
}

function getRandomQuestions(subject, num) {
    const shuffled = allQuestions[subject].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, num);
}

function displayQuestion() {
    const quizQuestionDiv = document.getElementById('quiz-question');
    const question = selectedQuestions[currentQuestionIndex];

    quizQuestionDiv.innerHTML = `
        <p>${currentQuestionIndex + 1}. ${question.question}</p>
        ${question.options.map((option, i) =>
            `<input type="radio" name="q${currentQuestionIndex}" value="${i}" ${userAnswers[currentQuestionIndex] == i ? 'checked' : ''}> ${option}<br>`).join('')}
    `;

    // Hide "Previous" button on the first question
    const prevButton = document.getElementById('prev-question');
    //if (currentQuestionIndex === 0) {
    //    prevButton.style.display = 'none';
  //  } else {
  //      prevButton.style.display = 'inline';
 //   }
    if (currentQuestionIndex === 0) {
    prevButton.disabled = true;  // Disable the button on the first question
        prevButton.style.display = 'none';
} else {
    prevButton.disabled = false; // Enable it on other questions
        prevButton.style.display = 'inline';
    }

    // Replace "Next" button with "Submit" on the last question
    const nextButton = document.getElementById('next-question');
    if (currentQuestionIndex === selectedQuestions.length - 1) {
        nextButton.innerText = 'Submit Quiz';
        nextButton.onclick = submitQuiz;
    } else {
        nextButton.innerText = 'Next';
        nextButton.onclick = nextQuestion;
    }
}


function nextQuestion() {
    saveAnswer();
    if (currentQuestionIndex < selectedQuestions.length - 1) {
        currentQuestionIndex++;
        displayQuestion();
    }
}

function prevQuestion() {
    saveAnswer();
    if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        displayQuestion();
    }
}

function saveAnswer() {
    const options = document.getElementsByName(`q${currentQuestionIndex}`);
    options.forEach(option => {
        if (option.checked) {
            userAnswers[currentQuestionIndex] = option.value;
        }
    });
}

function displayQuestionNumbers() {
    const questionNumbersDiv = document.getElementById('question-numbers');
    questionNumbersDiv.innerHTML = selectedQuestions.map((q, index) =>
        `<button onclick="goToQuestion(${index})">${index + 1}</button>`
    ).join(' ');
}

function goToQuestion(index) {
    saveAnswer();
    currentQuestionIndex = index;
    displayQuestion();
}

function submitQuiz() {
    saveAnswer();
    let score = 0;
    const correctAnswersDiv = document.getElementById('correct-answers');
    correctAnswersDiv.innerHTML = '';

    selectedQuestions.forEach((q, index) => {
        const userAnswer = userAnswers[index];
        if (userAnswer == q.correctAnswer) {
            score++;
        } else {
            correctAnswersDiv.innerHTML += `<p>${index + 1}. ${q.question}<br>Correct answer: ${q.options[q.correctAnswer]}</p>`;
        }
    });

    document.getElementById('score').innerText = `You scored ${score} out of ${selectedQuestions.length}`;
    document.getElementById('quiz-container').classList.add('hidden');
    document.getElementById('quiz-navigation').classList.add('hidden');
    document.getElementById('quiz-results').classList.remove('hidden');
    stopTimer();
}

function restartQuiz() {
    document.getElementById('quiz-results').classList.add('hidden');
    document.getElementById('quiz-settings').classList.remove('hidden');
    document.getElementById('timer-container').classList.add('hidden');
    stopTimer();
}

function startTimer(seconds) {
    let timeLeft = seconds;
    const timerElement = document.getElementById('timer');

    quizTimer = setInterval(() => {
        if (timeLeft <= 0) {
            clearInterval(quizTimer);
            alert("Time's up! Submitting your quiz.");
            submitQuiz();
        } else {
            let minutes = Math.floor(timeLeft / 60);
            let seconds = timeLeft % 60;
            timerElement.textContent = `Time left: ${minutes}:${seconds < 10 ? '0' : ''}${seconds}`;
            timeLeft--;
        }
    }, 1000);
}

function stopTimer() {
    if (quizTimer) {
        clearInterval(quizTimer);
    }
}
